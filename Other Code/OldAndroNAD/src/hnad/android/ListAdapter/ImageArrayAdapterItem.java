package hnad.android.ListAdapter;

/**
 * This class define the method necessary to get the image for an array adapter item.
 * @author Cory Sohrakoff
 *
 */
public interface ImageArrayAdapterItem {

	/**
	 * Get the image resource id (drawable) for the item.
	 * @return drawable resource id.
	 */
	public int getImageResource();
}
